# include<stdio.h>

// 测试集成初始化
int main()
{
	int a[] = {2,4,6,7,1,3,5,9,11,13,23,14,32};
	int b[13] = {2};
	int c[13] = {[1]=2,4,[5]=6};
	int i;
	printf("a[] = {2,4,...,14,32:\t");
	for (i=0;i<13;i++){
		printf("%d,", a[i]);
	}
	printf("\nb[13] = {2}:\t\t");
	for (i=0;i<13;i++){
		printf("%d,", b[i]);
	}
	printf("\nc[13] = {[1]=2,4,[5]=6}:");
	for (i=0;i<13;i++){
		printf("%d,", c[i]);
	}
	
}
